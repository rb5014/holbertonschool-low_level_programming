#include "3-calc.h"
/**
 * op_add - operate the sum of a and b
 * @a: first num
 * @b: second num
 * Return: result of the operation
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - operate the sub of a and b
 * @a: first num
 * @b: second num
 * Return: result of the operation
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - operate the mul of a and b
 * @a: first num
 * @b: second num
 * Return: result of the operation
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - operate the div of a and b
 * @a: first num
 * @b: second num
 * Return: result of the operation
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - operate the mod of a and b
 * @a: first num
 * @b: second num
 * Return: result of the operation
 */

int op_mod(int a, int b)
{
	return (a % b);
}
